<script setup>
import { onBeforeUnmount, onMounted, ref } from 'vue';
const emit = defineEmits(['covered', 'finished']);

const svgRef = ref(null);
const logoWrapRef = ref(null);

const CLIP_DURATION_MS = 420;
const BASE_ANIMATION_MS = 3000;

let resizeRaf = null;
let revealTimer = null;
let collapseTimer = null;
let finishTimer = null;

function getPaths() {
    return svgRef.value ? Array.from(svgRef.value.querySelectorAll('path')) : [];
}

function setupStrokeAnimation() {
    const paths = getPaths();
    if (!paths.length) return;

    const totalMs = BASE_ANIMATION_MS;
    const drawPhaseMs = 2100;
    const drawDurationMs = 1200;
    const fillStartMs = drawPhaseMs;
    const staggerMs = paths.length > 1
        ? (drawPhaseMs - drawDurationMs) / (paths.length - 1)
        : 0;

    paths.forEach((path, index) => {
        const length = path.getTotalLength();

        path.style.strokeDasharray = `${length}`;
        path.style.strokeDashoffset = `${length}`;

        const drawDelayMs = index * staggerMs;
        const fillDelayMs = fillStartMs;

        path.style.transitionDelay = [
            `${drawDelayMs}ms`,
            `${fillDelayMs}ms`
        ].join(', ');
    });

    return totalMs;
}

function getTotalAnimationTime() {
    return BASE_ANIMATION_MS;
}

function resetAnimation() {
    const svg = svgRef.value;
    const paths = getPaths();

    if (!svg || !paths.length) return;

    svg.classList.remove('active');
    logoWrapRef.value?.classList.remove('is-revealed', 'is-collapsing');

    paths.forEach((path) => {
        path.style.transition = 'none';
        const length = path.getTotalLength();
        path.style.strokeDasharray = `${length}`;
        path.style.strokeDashoffset = `${length}`;
        path.getBoundingClientRect();
        path.style.transition = '';
    });
}

function playAnimation() {
    const svg = svgRef.value
    const paths = getPaths()

    if (!svg || !paths.length) return

    resetAnimation()
    const totalMs = setupStrokeAnimation() ?? getTotalAnimationTime();

    requestAnimationFrame(() => {
        requestAnimationFrame(() => {
            logoWrapRef.value?.classList.add('is-revealed');
        })
    })

    clearTimeout(revealTimer);
    revealTimer = window.setTimeout(() => {
        emit('covered');
        svg.classList.add('active');
    }, CLIP_DURATION_MS);

    clearTimeout(finishTimer);
    clearTimeout(collapseTimer);
    collapseTimer = window.setTimeout(() => {
        logoWrapRef.value?.classList.add('is-collapsing');
    }, CLIP_DURATION_MS + totalMs);

    finishTimer = window.setTimeout(() => {
        emit('finished');
    }, CLIP_DURATION_MS + totalMs + CLIP_DURATION_MS);
}

function replay() {
    playAnimation()
}

function handleResize() {
    if (resizeRaf) cancelAnimationFrame(resizeRaf)

    resizeRaf = requestAnimationFrame(() => {
        const svg = svgRef.value
        const paths = getPaths()

        if (!svg || !paths.length) return

        const wasActive = svg.classList.contains('active')
        setupStrokeAnimation()

        if (wasActive) {
            paths.forEach((path) => {
                path.style.strokeDashoffset = '0'
            })
            svg.classList.add('active')
        }

    })
}

onMounted(() => {
    setupStrokeAnimation()
    playAnimation()
    window.addEventListener('resize', handleResize)
})

onBeforeUnmount(() => {
    clearTimeout(revealTimer)
    clearTimeout(collapseTimer)
    clearTimeout(finishTimer)
    if (resizeRaf) cancelAnimationFrame(resizeRaf)
    window.removeEventListener('resize', handleResize)
})

defineExpose({
    replay,
    playAnimation,
    resetAnimation
})
</script>

